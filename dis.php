<div id="slider1">
<div class="viewport">
<ul class="overview" style="width: 2118px; left: -830.664px;">
<li> <img src="http://vikaspedia.in/social-welfare/sw1" alt="sw1" title="Vikaspedia">
<h3>Empowering marginalized and vulnerable communities.</h3>
<p>The Indian government has established an extensive social welfare system. Several programmes designed for betterment and enhancement of quality of life for SC, ST, BC, Minorities, women, etc stand proof to it.</p>
</li>
<li> <img src="http://vikaspedia.in/social-welfare/sw2" alt="sw2" title="Vikaspedia">
<h3>Sensitize common man about ongoing schemes and programmes.</h3>
<p>Every citizen need to be aware about his / her entitlements, relevant schemes and programmes being implemented by Central and State Governments so as to access and benefit from them.</p>
</li>
